even = [2, 4, 6, 8]
odd = [1, 3, 5, 7, 9]
river = ["mississipi"]
# max() and min() functions return the max and min values of a list respectively
print(max(even))
print(min(odd))

# len() function returns the length of a list
print(len(odd))

# count(arg) function returns the number of times the arg appears in the list
print(str(river).count("pi"))
print("mississipi".count("s"))

